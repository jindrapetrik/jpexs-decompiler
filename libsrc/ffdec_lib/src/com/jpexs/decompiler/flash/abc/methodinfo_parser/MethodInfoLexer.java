    /**
     * Returns the text matched by the current regular expression.
     */
    public final String yytext() {
        return new String(zzBuffer, zzStartRead, zzMarkedPos - zzStartRead);
    }

    /**
     * Returns the character at position <tt>pos</tt> from the matched text.
     *
     * It is equivalent to yytext().charAt(pos), but faster
     *
     * @param pos the position of the character to fetch. A value from 0 to
     * yylength()-1.
     *
     * @return the character at position pos
     */
    public final char yycharat(int pos) {
        return zzBuffer[zzStartRead + pos];
    }

    /**
     * Returns the length of the matched text region.
     */
    public final int yylength() {
        return zzMarkedPos - zzStartRead;
    }

    /**
     * Reports an error that occured while scanning.
     *
     * In a wellformed scanner (no or only correct usage of yypushback(int) and
     * a match-all fallback rule) this method will only be called with things
     * that "Can't Possibly Happen". If this method is called, something is
     * seriously wrong (e.g. a JFlex bug producing a faulty scanner etc.).
     *
     * Usual syntax/scanner level error handling should be done in error
     * fallback rules.
     *
     * @param errorCode the code of the errormessage to display
     */
    private void zzScanError(int errorCode) {
        String message;
        try {
            message = ZZ_ERROR_MSG[errorCode];
        } catch (ArrayIndexOutOfBoundsException e) {
            message = ZZ_ERROR_MSG[ZZ_UNKNOWN_ERROR];
        }

        throw new Error(message);
    }

    /**
     * Pushes the specified amount of characters back into the input stream.
     *
     * They will be read again by then next call of the scanning method
     *
     * @param number the number of characters to be read again. This number must
     * not be greater than yylength()!
     */
    public void yypushback(int number) {
        if (number > yylength()) {
            zzScanError(ZZ_PUSHBACK_2BIG);
        }

        zzMarkedPos -= number;
    }

    /**
     * Resumes scanning until the next regular expression is matched, the end of
     * input is encountered or an I/O-Error occurs.
     *
     * @return the next token
     * @exception java.io.IOException if any I/O-Error occurs
     */
    public ParsedSymbol yylex() throws java.io.IOException, MethodInfoParseException {
        int zzInput;
        int zzAction;

        // cached fields:
        int zzCurrentPosL;
        int zzMarkedPosL;
        int zzEndReadL = zzEndRead;
        char[] zzBufferL = zzBuffer;
        char[] zzCMapL = ZZ_CMAP;

        int[] zzTransL = ZZ_TRANS;
        int[] zzRowMapL = ZZ_ROWMAP;
        int[] zzAttrL = ZZ_ATTRIBUTE;

        while (true) {
            zzMarkedPosL = zzMarkedPos;

            yychar += zzMarkedPosL - zzStartRead;

            boolean zzR = false;
            int zzCh;
            int zzCharCount;
            for (zzCurrentPosL = zzStartRead;
                    zzCurrentPosL < zzMarkedPosL;
                    zzCurrentPosL += zzCharCount) {
                zzCh = Character.codePointAt(zzBufferL, zzCurrentPosL, zzMarkedPosL);
                zzCharCount = Character.charCount(zzCh);
                switch (zzCh) {
                    case '\u000B':
                    case '\u000C':
                    case '\u0085':
                    case '\u2028':
                    case '\u2029':
                        yyline++;
                        yycolumn = 0;
                        zzR = false;
                        break;
                    case '\r':
                        yyline++;
                        yycolumn = 0;
                        zzR = true;
                        break;
                    case '\n':
                        if (zzR) {
                            zzR = false;
                        } else {
                            yyline++;
                            yycolumn = 0;
                        }
                        break;
                    default:
                        zzR = false;
                        yycolumn += zzCharCount;
                }
            }

            if (zzR) {
                // peek one character ahead if it is \n (if we have counted one line too much)
                boolean zzPeek;
                if (zzMarkedPosL < zzEndReadL) {
                    zzPeek = zzBufferL[zzMarkedPosL] == '\n';
                } else if (zzAtEOF) {
                    zzPeek = false;
                } else {
                    boolean eof = zzRefill();
                    zzEndReadL = zzEndRead;
                    zzMarkedPosL = zzMarkedPos;
                    zzBufferL = zzBuffer;
                    if (eof) {
                        zzPeek = false;
                    } else {
                        zzPeek = zzBufferL[zzMarkedPosL] == '\n';
                    }
                }
                if (zzPeek) {
                    yyline--;
                }
            }
            zzAction = -1;

            zzCurrentPosL = zzCurrentPos = zzStartRead = zzMarkedPosL;

            zzState = ZZ_LEXSTATE[zzLexicalState];

            // set up zzAction for empty match case:
            int zzAttributes = zzAttrL[zzState];
            if ((zzAttributes & 1) == 1) {
                zzAction = zzState;
            }

            zzForAction:
            {
                while (true) {

                    if (zzCurrentPosL < zzEndReadL) {
                        zzInput = Character.codePointAt(zzBufferL, zzCurrentPosL, zzEndReadL);
                        zzCurrentPosL += Character.charCount(zzInput);
                    } else if (zzAtEOF) {
                        zzInput = YYEOF;
                        break zzForAction;
                    } else {
                        // store back cached positions
                        zzCurrentPos = zzCurrentPosL;
                        zzMarkedPos = zzMarkedPosL;
                        boolean eof = zzRefill();
                        // get translated positions and possibly new buffer
                        zzCurrentPosL = zzCurrentPos;
                        zzMarkedPosL = zzMarkedPos;
                        zzBufferL = zzBuffer;
                        zzEndReadL = zzEndRead;
                        if (eof) {
                            zzInput = YYEOF;
                            break zzForAction;
                        } else {
                            zzInput = Character.codePointAt(zzBufferL, zzCurrentPosL, zzEndReadL);
                            zzCurrentPosL += Character.charCount(zzInput);
                        }
                    }
                    int zzNext = zzTransL[zzRowMapL[zzState] + zzCMapL[zzInput]];
                    if (zzNext == -1) {
                        break zzForAction;
                    }
                    zzState = zzNext;

                    zzAttributes = zzAttrL[zzState];
                    if ((zzAttributes & 1) == 1) {
                        zzAction = zzState;
                        zzMarkedPosL = zzCurrentPosL;
                        if ((zzAttributes & 8) == 8) {
                            break zzForAction;
                        }
                    }

                }
            }

            // store back cached position
            zzMarkedPos = zzMarkedPosL;

            switch (zzAction < 0 ? zzAction : ZZ_ACTION[zzAction]) {
                case 1: {
                }
                case 36:
                    break;
                case 2: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_IDENTIFIER, yytext());
                }
                case 37:
                    break;
                case 3: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_INTEGER, Long.parseLong((yytext())));
                }
                case 38:
                    break;
                case 4: {
                    isMultiname = false;
                    yybegin(STRING);
                    string.setLength(0);
                }
                case 39:
                    break;
                case 5: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_COLON);
                }
                case 40:
                    break;
                case 6: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_COMMA);
                }
                case 41:
                    break;
                case 7: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_STAR);
                }
                case 42:
                    break;
                case 8: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_ASSIGN);
                }
                case 43:
                    break;
                case 9: {
                    string.append(yytext());
                }
                case 44:
                    break;
                case 10: {
                    throw new MethodInfoParseException("Unterminated string at end of line", yyline + 1);
                }
                case 45:
                    break;
                case 11: {
                    yybegin(YYINITIAL);
                    // length also includes the trailing quote
                    if (isMultiname) {
                        return new ParsedSymbol(ParsedSymbol.TYPE_MULTINAME, multinameId);
                    } else {
                        return new ParsedSymbol(ParsedSymbol.TYPE_STRING, string.toString());
                    }
                }
                case 46:
                    break;
                case 12: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_FLOAT, Double.parseDouble((yytext())));
                }
                case 47:
                    break;
                case 13: {
                    throw new MethodInfoParseException("Illegal escape sequence \"" + yytext() + "\"", yyline + 1);
                }
                case 48:
                    break;
                case 14: {
                    string.append('\n');
                }
                case 49:
                    break;
                case 15: {
                    string.append('\t');
                }
                case 50:
                    break;
                case 16: {
                    string.append('\b');
                }
                case 51:
                    break;
                case 17: {
                    string.append('\r');
                }
                case 52:
                    break;
                case 18: {
                    string.append('\\');
                }
                case 53:
                    break;
                case 19: {
                    string.append('\"');
                }
                case 54:
                    break;
                case 20: {
                    string.append('\f');
                }
                case 55:
                    break;
                case 21: {
                    string.append('\'');
                }
                case 56:
                    break;
                case 22: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_DOTS);
                }
                case 57:
                    break;
                case 23: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_NULL);
                }
                case 58:
                    break;
                case 24: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_TRUE);
                }
                case 59:
                    break;
                case 25: {
                    char val = (char) Integer.parseInt(yytext().substring(2), 16);
                    string.append(val);
                }
                case 60:
                    break;
                case 26: {
                    isMultiname = true;
                    String s = yytext();
                    multinameId = Long.parseLong(s.substring(2, s.length() - 2));
                    yybegin(STRING);
                    string.setLength(0);
                }
                case 61:
                    break;
                case 27: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_FALSE);
                }
                case 62:
                    break;
                case 28: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_STATIC);
                }
                case 63:
                    break;
                case 29: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_PRIVATE);
                }
                case 64:
                    break;
                case 30: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_PACKAGE);
                }
                case 65:
                    break;
                case 31: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_INTERNAL);
                }
                case 66:
                    break;
                case 32: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_EXPLICIT);
                }
                case 67:
                    break;
                case 33: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_UNDEFINED);
                }
                case 68:
                    break;
                case 34: {
                    return new ParsedSymbol(ParsedSymbol.TYPE_PROTECTED);
                }
                case 69:
                    break;
                case 35: {
                    String s = yytext();
                    long ns = Long.parseLong(s.substring(3, s.length() - 2));
                    return new ParsedSymbol(ParsedSymbol.TYPE_NAMESPACE, ns);
                }
                case 70:
                    break;
                default:
                    if (zzInput == YYEOF && zzStartRead == zzCurrentPos) {
                        zzAtEOF = true;
                        {
                            return new ParsedSymbol(ParsedSymbol.TYPE_EOF);
                        }
                    } else {
                        zzScanError(ZZ_NO_MATCH);
                    }
            }
        }
    }
