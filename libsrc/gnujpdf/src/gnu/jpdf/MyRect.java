package gnu.jpdf;

/**
 *
 * @author JPEXS
 */
public class MyRect {

    public int xMin;
    public int yMin;
    public int xMax;
    public int yMax;

    public MyRect(int xMin, int yMin, int xMax, int yMax) {
        this.xMin = xMin;
        this.yMin = yMin;
        this.xMax = xMax;
        this.yMax = yMax;
    }

}
